/**
 * Creates an example PDF TEST document using TCPDF
 * @package com.tecnick.tcpdf
 * @abstract TCPDF - Example: WriteHTML text flow.
 * @author Nicola Asuni
 * @since 2008-03-04
 */

require_once('eng.php');
require_once('tcpdf.php');

// create new PDF document
$pdf = new TCPDF(PDF_PAGE_ORIENTATION, PDF_UNIT, PDF_PAGE_FORMAT, true, 'UTF-8', false);

// set document information
$pdf->SetCreator(PDF_CREATOR);
$pdf->SetAuthor('Nicola Asuni');
$pdf->SetTitle('TCPDF Example 021');
$pdf->SetSubject('TCPDF Tutorial');
$pdf->SetKeywords('TCPDF, PDF, example, test, guide');

// set default header data
$pdf->SetHeaderData("", PDF_HEADER_LOGO_WIDTH, "", PDF_HEADER_STRING);

// set header and footer fonts
$pdf->setHeaderFont(Array(PDF_FONT_NAME_MAIN, '', PDF_FONT_SIZE_MAIN));
$pdf->setFooterFont(Array(PDF_FONT_NAME_DATA, '', PDF_FONT_SIZE_DATA));

// set default monospaced font
$pdf->SetDefaultMonospacedFont(PDF_FONT_MONOSPACED);

//set margins
$pdf->SetMargins(PDF_MARGIN_LEFT, PDF_MARGIN_TOP, PDF_MARGIN_RIGHT);
$pdf->SetHeaderMargin(PDF_MARGIN_HEADER);
$pdf->SetFooterMargin(PDF_MARGIN_FOOTER);

//set auto page breaks
$pdf->SetAutoPageBreak(TRUE, PDF_MARGIN_BOTTOM);

//set image scale factor
$pdf->setImageScale(PDF_IMAGE_SCALE_RATIO);

//set some language-dependent strings
$pdf->setLanguageArray($l);

// ---------------------------------------------------------

// set font
$pdf->SetFont('helvetica', '', 9);

// add a page
$pdf->AddPage();

// create some HTML content
$html = '<h3>OBJECTIVE:</h3><br />'.$objective .'<br /><br /><h3>PROFESSIONAL WORK HISTORY:</h3><br /><b>'.$JobTitle1.',&nbsp;<i>'.$Employer1.'</i></b>,&nbsp;'.$City1.',&nbsp;'.$empstate1.'&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;'.$FromDate1.'&nbsp;to&nbsp;'.$ThruDate1.'<br /><br/>&nbsp;'.$Duties1.'<br/><br/><h3>EDUCATIONAL BACKGROUND:</h3><br /><b>'.$School1.'</b>,&nbsp;'.$SchoolCity1.',&nbsp;'.$schoolstate1.'<br/>'.$SchoolDegree1.'<br/><h3>SPECIAL TRAINING:</h3><br />'.$specialtraining .'<br /><br /><h3>HONORS & AWARDS:</h3><br />'.$honers .'<br /><br /><h3>ADDITIONAL INFORMATION:</h3><br />'.$additional .'<br /><br />';

// output the HTML content
$pdf->writeHTML($html, true, 0, true, 0);

// reset pointer to the last page
$pdf->lastPage();

// ---------------------------------------------------------

//Close and output PDF document
$pdf->Output('example_021.pdf', 'I');

